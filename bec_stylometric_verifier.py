def evaluate_model(model, X, y_true, le=None, model_type='xgb', label=''):
    """
    Evaluate a trained model and print accuracy + classification report.

    Args:
        model:      Fitted XGBClassifier or LogisticRegression
        X:          Feature matrix or embedding array
        y_true:     True user labels (string)
        le:         LabelEncoder (required for XGBoost)
        model_type: 'xgb' or 'st'
        label:      Description for print header

    Returns:
        y_pred: Predicted user labels (string)
        acc:    Accuracy score
    """
    if model_type == 'xgb':
        y_pred_enc = model.predict(X)
        y_pred = le.inverse_transform(y_pred_enc)
    else:
        y_pred = model.predict(X)

    acc = accuracy_score(y_true, y_pred)
    print(f"\n=== {label} ===")
    print(f"Accuracy: {acc:.4f}")
    print(classification_report(y_true, y_pred, digits=4))
    return y_pred, acc


