def parse_email(raw_message):
    """Extract date, subject, and body from a raw RFC 2822 email string."""
    try:
        msg = email_lib.message_from_string(raw_message)

        date = msg.get('Date', None)
        subject = msg.get('Subject', '')

        body = ''
        if msg.is_multipart():
            for part in msg.walk():
                if part.get_content_type() == 'text/plain':
                    body = part.get_payload(decode=True)
                    if body:
                        body = body.decode('utf-8', errors='ignore')
                    break
        else:
            body = msg.get_payload(decode=True)
            if body:
                body = body.decode('utf-8', errors='ignore')
            else:
                body = msg.get_payload()

        return pd.Series({
            'date': date,
            'subject': subject,
            'body': body if body else ''
        })
    except Exception:
        return pd.Series({'date': None, 'subject': '', 'body': ''})


def clean_body(text):
    """
    Remove quoted reply chains, forwarded blocks, and metadata headers.

    v2 Update: Added removal of Enron-specific forwarded message headers,
    company announcement separators, and residual email header lines.
    These were contaminating feature vectors for rogers-b and shapiro-r.
    """
    if not isinstance(text, str):
        return ''

    # Remove quoted lines starting with >
    lines = text.split('\n')
    lines = [l for l in lines if not l.strip().startswith('>')]
    text = '\n'.join(lines)

    # Remove Original Message blocks
    text = re.sub(r'-+\s*Original Message\s*-+.*', '',
                  text, flags=re.DOTALL | re.IGNORECASE)
    text = re.sub(r'_{5,}.*', '', text, flags=re.DOTALL)

    # Remove Enron-specific forwarded message headers
    text = re.sub(r'-+\s*Forwarded by.*?-+\s*', ' ',
                  text, flags=re.DOTALL | re.IGNORECASE)

    # Remove company announcement separators
    text = re.sub(r'={5,}.*?={5,}', ' ', text, flags=re.DOTALL)

    # Remove residual email header lines
    lines = text.split('\n')
    lines = [l for l in lines if not re.match(
        r'^\s*(From|To|cc|Subject|Date|Sent|Importance):\s', l)]
    text = '\n'.join(lines)

    # Clean whitespace
    text = re.sub(r'\s+', ' ', text).strip()
    return text


def load_and_preprocess(csv_path):
    """
    Load Enron CSV, filter to selected users, parse emails, clean bodies,
    apply the 20-word minimum, and parse dates.

    Args:
        csv_path: Path to Kaggle Enron emails.csv

    Returns:
        df_clean: Preprocessed DataFrame
    """
    df = pd.read_csv(csv_path)
    df['user'] = df['file'].apply(lambda x: x.split('/')[0])

    df_filtered = df[df['user'].isin(SELECTED_USERS)].copy()

    tqdm.pandas()
    parsed = df_filtered.progress_apply(
        lambda row: parse_email(row['message']), axis=1
    )

    df_parsed = pd.concat([
        df_filtered[['user', 'file']].reset_index(drop=True),
        parsed.reset_index(drop=True)
    ], axis=1)

    df_parsed['body_clean'] = df_parsed['body'].apply(clean_body)
    df_parsed['word_count'] = df_parsed['body_clean'].apply(
        lambda x: len(x.split()))
    df_clean = df_parsed[df_parsed['word_count'] >= 20].copy()

    df_clean['date_parsed'] = pd.to_datetime(
        df_clean['date'], errors='coerce', utc=True)
    df_clean = df_clean.dropna(subset=['date_parsed'])

    return df_clean


def get_chronological_splits(df, user, train=0.6, test=0.2):
    """
    Chronological 60/20/20 split for a single user.

    Args:
        df:    Full DataFrame containing emails for all users
        user:  User identifier string
        train: Fraction for training split
        test:  Fraction for test split

    Returns:
        Tuple of (train_df, test_df, eval_df) for this user
    """
    u = df[df['user'] == user].sort_values('date_parsed').reset_index(drop=True)
    n = len(u)
    t1 = int(n * train)
    t2 = int(n * (train + test))
    return (u.iloc[:t1].assign(split='train'),
            u.iloc[t1:t2].assign(split='test'),
            u.iloc[t2:].assign(split='eval'))


def build_splits(df_clean):
    """
    Build chronological train/test/eval splits for all users.
    Filters to the realistic Enron date range (1999-2002) and
    deduplicates within each split.

    Returns:
        train_df, test_df, eval_df
    """
    df_clean = df_clean[
        (df_clean['date_parsed'] >= '1999-01-01') &
        (df_clean['date_parsed'] <= '2002-12-31')
    ].copy()

    splits = [get_chronological_splits(df_clean, u) for u in SELECTED_USERS]
    train_df = pd.concat([s[0] for s in splits]).reset_index(drop=True)
    test_df  = pd.concat([s[1] for s in splits]).reset_index(drop=True)
    eval_df  = pd.concat([s[2] for s in splits]).reset_index(drop=True)

    train_df = train_df.drop_duplicates(
        subset=['user', 'body_clean']).reset_index(drop=True)
    test_df  = test_df.drop_duplicates(
        subset=['user', 'body_clean']).reset_index(drop=True)
    eval_df  = eval_df.drop_duplicates(
        subset=['user', 'body_clean']).reset_index(drop=True)

    return train_df, test_df, eval_df


