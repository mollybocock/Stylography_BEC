def extract_lexical_features(text):
    """
    Extract lexical stylometric features.

    Includes type-token ratio, mean word length, hapax legomena ratio,
    and frequency of 108 function words. Function words encode style,
    not content — they are topic-independent and largely unconscious.
    (Mosteller & Wallace, 1964; Koppel et al., 2011)
    """
    if not isinstance(text, str) or len(text.strip()) == 0:
        return {}

    words = text.lower().split()
    if len(words) == 0:
        return {}

    alpha_words = [w for w in words if w.isalpha()]
    features = {}

    features['type_token_ratio'] = (
        len(set(alpha_words)) / len(alpha_words) if alpha_words else 0)

    features['mean_word_length'] = (
        np.mean([len(w) for w in alpha_words]) if alpha_words else 0)

    word_counts = {}
    for w in alpha_words:
        word_counts[w] = word_counts.get(w, 0) + 1
    hapax = sum(1 for c in word_counts.values() if c == 1)
    features['hapax_ratio'] = hapax / len(alpha_words) if alpha_words else 0

    for fw in FUNCTION_WORDS:
        features[f'fw_{fw}'] = words.count(fw) / len(words)

    return features


def extract_syntactic_features(text):
    """
    Extract syntactic features via spaCy.

    Captures sentence length distribution and POS ratios (noun, verb,
    adjective, adverb, pronoun). Stable within a person across topics.
    (Stamatatos, 2009)
    """
    if not isinstance(text, str) or len(text.strip()) == 0:
        return {}

    doc = nlp(text)
    sentences = list(doc.sents)

    if len(sentences) == 0:
        return {}

    features = {}

    sent_lengths = [len([t for t in s if not t.is_space]) for s in sentences]
    features['mean_sent_length'] = np.mean(sent_lengths)
    features['var_sent_length']  = np.var(sent_lengths)
    features['max_sent_length']  = np.max(sent_lengths)

    tokens = [t for t in doc if not t.is_space and not t.is_punct]
    if len(tokens) == 0:
        return features

    pos_counts = {}
    for t in tokens:
        pos_counts[t.pos_] = pos_counts.get(t.pos_, 0) + 1

    features['noun_ratio'] = pos_counts.get('NOUN', 0) / len(tokens)
    features['verb_ratio'] = pos_counts.get('VERB', 0) / len(tokens)
    features['adj_ratio']  = pos_counts.get('ADJ', 0)  / len(tokens)
    features['adv_ratio']  = pos_counts.get('ADV', 0)  / len(tokens)
    features['pron_ratio'] = pos_counts.get('PRON', 0) / len(tokens)

    return features


def extract_structural_features(text):
    """
    Extract email-specific structural features.

    Covers salutation presence, sign-off style, word/char/paragraph/line
    counts. Salutation and sign-off conventions are highly individual and
    remarkably stable across a person's emails. (Klimt & Yang, 2004)
    """
    if not isinstance(text, str) or len(text.strip()) == 0:
        return {}

    features = {}
    lines = [l.strip() for l in text.split('\n') if l.strip()]
    words = text.split()

    has_salutation = 0
    if lines:
        first_line = lines[0].lower()
        for sal in SALUTATIONS:
            if first_line.startswith(sal):
                has_salutation = 1
                break
    features['has_salutation'] = has_salutation

    has_signoff = 0
    if lines:
        last_line = lines[-1].lower()
        for sig in SIGNOFFS:
            if last_line.startswith(sig):
                has_signoff = 1
                break
    features['has_signoff'] = has_signoff

    features['word_count']      = len(words)
    features['char_count']      = len(text)
    features['paragraph_count'] = len([p for p in text.split('\n\n') if p.strip()])
    features['line_count']      = len(lines)

    return features


def extract_punctuation_features(text):
    """
    Extract punctuation and orthographic features.

    Rates for exclamation marks, question marks, commas, semicolons,
    dashes, ellipses, contractions, and capitalized words. Punctuation
    habits operate below conscious awareness. (Stamatatos, 2009)
    """
    if not isinstance(text, str) or len(text.strip()) == 0:
        return {}

    features = {}
    sentences = [s for s in text.split('.') if s.strip()]
    n_sents = max(len(sentences), 1)
    n_words = max(len(text.split()), 1)

    features['exclamation_rate'] = text.count('!') / n_sents
    features['question_rate']    = text.count('?') / n_sents
    features['comma_rate']       = text.count(',') / n_sents
    features['semicolon_rate']   = text.count(';') / n_sents
    features['dash_rate']        = (text.count('--') + text.count('—')) / n_sents
    features['ellipsis_rate']    = text.count('...') / n_sents

    contractions = ["n't", "'re", "'ve", "'ll", "'d", "'m", "'s"]
    contraction_count = sum(text.lower().count(c) for c in contractions)
    features['contraction_rate'] = contraction_count / n_words

    words = text.split()
    if words:
        upper_words = sum(1 for w in words if w.isupper() and len(w) > 1)
        features['caps_ratio'] = upper_words / len(words)
    else:
        features['caps_ratio'] = 0

    return features


def extract_pragmatic_features(text):
    """
    Extract pragmatic and register features.

    Covers hedging rate, politeness marker rate, first-person singular/
    plural rate, question ratio, and imperative ratio. Reflects how a
    person positions themselves communicatively. (Abbasi & Chen, 2008)
    """
    if not isinstance(text, str) or len(text.strip()) == 0:
        return {}

    features = {}
    text_lower = text.lower()
    words = text_lower.split()
    n_words = max(len(words), 1)
    sentences = [s for s in text.split('.') if s.strip()]
    n_sents = max(len(sentences), 1)

    hedge_count = sum(text_lower.count(h) for h in HEDGE_PHRASES)
    features['hedge_rate'] = hedge_count / n_sents

    polite_count = sum(text_lower.count(p) for p in POLITENESS_MARKERS)
    features['politeness_rate'] = polite_count / n_sents

    first_person = (words.count('i') + words.count("i'm") +
                    words.count("i've") + words.count("i'll"))
    features['first_person_rate'] = first_person / n_words

    first_plural = (words.count('we') + words.count("we're") +
                    words.count("we've") + words.count("we'll"))
    features['first_plural_rate'] = first_plural / n_words

    question_sents = sum(1 for s in sentences if '?' in s)
    features['question_ratio'] = question_sents / n_sents

    doc = nlp(text[:500])
    imperative_count = 0
    for sent in doc.sents:
        tokens = [t for t in sent if not t.is_space]
        if tokens and tokens[0].pos_ == 'VERB':
            imperative_count += 1
    features['imperative_ratio'] = imperative_count / n_sents

    return features


def extract_all_features(text):
    """
    Combine all five stylometric feature families into one vector.

    Returns a flat dictionary of ~138 features per email:
        - Lexical (110):     function word frequencies, vocab richness
        - Syntactic (8):     sentence length stats, POS ratios
        - Structural (6):    salutation, sign-off, length
        - Punctuation (8):   exclamation, comma, ellipsis rates
        - Pragmatic (6):     hedging, politeness, person rates
    """
    features = {}
    features.update(extract_lexical_features(text))
    features.update(extract_syntactic_features(text))
    features.update(extract_structural_features(text))
    features.update(extract_punctuation_features(text))
    features.update(extract_pragmatic_features(text))
    return features


def build_feature_matrix(df, char_ngram_vectorizer, desc="Processing"):
    """
    Build the full 638-feature matrix for a DataFrame of emails.

    Combines 138 stylometric features with 500 character n-gram TF-IDF
    features. char_ngram_vectorizer must be fitted on training data before
    calling this function on test/eval splits to prevent data leakage.

    Args:
        df:                    DataFrame with 'body_clean' and 'user' columns
        char_ngram_vectorizer: Pre-fitted TfidfVectorizer
        desc:                  tqdm progress bar label

    Returns:
        combined: DataFrame of shape (n_emails, 639) — 638 features + 'user'
    """
    records = []
    for _, row in tqdm(df.iterrows(), total=len(df), desc=desc):
        features = extract_all_features(row['body_clean'])
        features['user'] = row['user']
        records.append(features)
    stylo_df = pd.DataFrame(records)

    # Use transform() — vectorizer already fitted on train
    ngram_matrix = char_ngram_vectorizer.transform(df['body_clean'])
    ngram_df = pd.DataFrame(
        ngram_matrix.toarray(),
        columns=[f'ng_{c}' for c in
                 char_ngram_vectorizer.get_feature_names_out()]
    ).reset_index(drop=True)

    combined = pd.concat([
        stylo_df.reset_index(drop=True),
        ngram_df
    ], axis=1)

    return combined


def fit_char_ngram_vectorizer(train_df):
    """
    Fit a character n-gram TF-IDF vectorizer on training data.

    Uses char_wb analyzer (word boundaries), n-gram range (3,5),
    top 500 features by TF-IDF score, sublinear TF scaling, min_df=5.
    Validated as the strongest single feature type (Stamatatos, 2009).
    CRITICAL: Always fit on training data only.
    """
    vectorizer = TfidfVectorizer(
        analyzer='char_wb',
        ngram_range=(3, 5),
        max_features=500,
        sublinear_tf=True,
        min_df=5
    )
    vectorizer.fit(train_df['body_clean'])
    return vectorizer


