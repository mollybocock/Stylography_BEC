def train_xgboost(X_train, y_train):
    """
    Train XGBoost classifier on 638-feature stylometric matrix.

    Architecture:
        n_estimators=200, learning_rate=0.1, max_depth=6,
        subsample=0.8, colsample_bytree=0.8

    XGBoost was chosen over Random Forest because it consistently
    outperforms RF on tabular stylometric data and is robust to
    irrelevant features and class imbalance. For feature importances
    used in red team briefs, a separate RF is trained (see Section 6).

    Returns:
        xgb_model: Fitted XGBClassifier
        le:        Fitted LabelEncoder (XGBoost requires numeric labels)
    """
    from xgboost import XGBClassifier

    le = LabelEncoder()
    y_train_enc = le.fit_transform(y_train)

    xgb_model = XGBClassifier(
        n_estimators=200,
        learning_rate=0.1,
        max_depth=6,
        subsample=0.8,
        colsample_bytree=0.8,
        use_label_encoder=False,
        eval_metric='mlogloss',
        random_state=42,
        n_jobs=-1
    )
    xgb_model.fit(X_train, y_train_enc)
    return xgb_model, le


def train_sentence_transformer(train_df, embeddings_path=None):
    """
    Train a Sentence Transformer + Logistic Regression classifier.

    Encodes each email with all-MiniLM-L6-v2 (384-dim embeddings),
    then fits a balanced Logistic Regression head. Pre-trained transformer
    is used because the Enron training set (~17,865 emails) is too small
    to train a transformer from scratch reliably.

    Args:
        train_df:       Training DataFrame with 'body_clean' and 'user'
        embeddings_path: Optional path to pre-saved .npy embeddings (skips
                         encoding step if provided)

    Returns:
        st_model:      SentenceTransformer encoder
        st_classifier: Fitted LogisticRegression head
    """
    from sentence_transformers import SentenceTransformer

    st_model = SentenceTransformer('sentence-transformers/all-MiniLM-L6-v2')

    if embeddings_path and os.path.exists(embeddings_path):
        X_train_st = np.load(embeddings_path)
    else:
        X_train_st = st_model.encode(
            train_df['body_clean'].tolist(),
            batch_size=64,
            show_progress_bar=True,
            convert_to_numpy=True
        )

    y_train_st = train_df['user'].values

    st_classifier = LogisticRegression(
        max_iter=1000,
        class_weight='balanced',
        random_state=42,
        n_jobs=-1
    )
    st_classifier.fit(X_train_st, y_train_st)
    return st_model, st_classifier


