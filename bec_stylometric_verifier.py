def run_pipeline(enron_csv_path, drive_base, openai_api_key):
    """
    End-to-end pipeline: preprocessing → features → models → red team.

    Args:
        enron_csv_path: Path to Kaggle Enron emails.csv
        drive_base:     Base directory for saving artifacts
        openai_api_key: OpenAI API key for GPT-4o impersonation calls

    Returns:
        all_results: DataFrame of evasion results across both conditions
                     and both classifiers
    """
    from openai import OpenAI

    os.makedirs(drive_base, exist_ok=True)
    client = OpenAI(api_key=openai_api_key)

    # --- Data ---
    print("Loading and preprocessing data...")
    df_clean = load_and_preprocess(enron_csv_path)
    train_df, test_df, eval_df = build_splits(df_clean)

    # --- Features ---
    print("Fitting character n-gram vectorizer...")
    char_ngram_vectorizer = fit_char_ngram_vectorizer(train_df)

    print("Building feature matrices...")
    train_features = build_feature_matrix(
        train_df, char_ngram_vectorizer, "Train")
    test_features  = build_feature_matrix(
        test_df, char_ngram_vectorizer, "Test")
    eval_features  = build_feature_matrix(
        eval_df, char_ngram_vectorizer, "Eval")

    X_train = train_features.drop('user', axis=1)
    y_train = train_features['user']
    X_test  = test_features.drop('user', axis=1)
    y_test  = test_features['user']
    X_eval  = eval_features.drop('user', axis=1)
    y_eval  = eval_features['user']

    # --- XGBoost ---
    print("Training XGBoost...")
    xgb_model, le = train_xgboost(X_train, y_train)
    evaluate_model(xgb_model, X_test, y_test, le,
                   model_type='xgb', label='XGBoost Test')

    # --- Sentence Transformer ---
    print("Training Sentence Transformer...")
    st_model, st_classifier = train_sentence_transformer(train_df)

    # --- Final eval (run once) ---
    print("Final eval set evaluation...")
    evaluate_model(xgb_model, X_eval, y_eval, le,
                   model_type='xgb', label='XGBoost Eval')

    X_eval_st = st_model.encode(
        eval_df['body_clean'].tolist(),
        batch_size=64, show_progress_bar=True, convert_to_numpy=True)
    evaluate_model(st_classifier, X_eval_st, y_eval,
                   model_type='st', label='Sentence Transformer Eval')

    # --- Red team ---
    print("Computing user profiles...")
    rf_model = RandomForestClassifier(
        n_estimators=500, min_samples_leaf=2,
        class_weight='balanced', random_state=42, n_jobs=-1)
    rf_model.fit(X_train, y_train)

    feature_names    = X_train.columns.tolist()
    user_profiles    = compute_user_profiles(train_features, feature_names)
    attack_briefs    = {u: generate_attack_brief(u, user_profiles[u])
                        for u in sorted(user_profiles)}

    print("Condition B: Statistical brief attacks...")
    impersonation_emails = {
        user: generate_impersonation(client, user, attack_briefs[user], n=5)
        for user in sorted(attack_briefs)
    }

    print("Condition C: Few-shot attacks...")
    fewshot_emails = {
        user: generate_impersonation(
            client, user,
            build_fewshot_brief(user, train_df, n_examples=5),
            n=5, condition='C')
        for user in sorted(attack_briefs)
    }

    # --- Evasion evaluation ---
    kwargs = dict(X_train_cols=X_train.columns,
                  char_ngram_vectorizer=char_ngram_vectorizer,
                  st_model=st_model)

    results_b_xgb = evaluate_impersonations(
        impersonation_emails, xgb_model, le=le,
        model_type='xgb', label='B_statistical_xgb', **kwargs)
    results_b_st  = evaluate_impersonations(
        impersonation_emails, st_classifier,
        model_type='st', label='B_statistical_st', **kwargs)
    results_c_xgb = evaluate_impersonations(
        fewshot_emails, xgb_model, le=le,
        model_type='xgb', label='C_fewshot_xgb', **kwargs)
    results_c_st  = evaluate_impersonations(
        fewshot_emails, st_classifier,
        model_type='st', label='C_fewshot_st', **kwargs)

    all_results = pd.concat([
        results_b_xgb, results_b_st,
        results_c_xgb, results_c_st
    ])

    print("\n=== Evasion Rate Summary ===")
    print(all_results.groupby('condition')['evaded'].mean().round(3))

    return all_results


